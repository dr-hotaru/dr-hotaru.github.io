function insertAfter(html, marker, insertion) {
  const idx = html.indexOf(marker);
  if (idx === -1) throw new Error(`Marker not found: ${marker}`);
  const pos = idx + marker.length;
  return html.slice(0, pos) + insertion + html.slice(pos);
}

function replaceMiniPost(indexHtml, genreKey, post) {
  const blockStart = indexHtml.indexOf(`<article class="genre-card ${genreKey}">`);
  if (blockStart === -1) throw new Error(`Genre card not found: ${genreKey}`);
  const blockEnd = indexHtml.indexOf(`</article>`, blockStart);
  const block = indexHtml.slice(blockStart, blockEnd);
  const miniStart = block.indexOf(`<a class="mini-post"`);
  const miniEnd = block.indexOf(`</a>`, miniStart) + `</a>`.length;
  const before = block.slice(0, miniStart);
  const after = block.slice(miniEnd);
  const mini = `<a class="mini-post" href="posts/${post.slug}.html"><img src="assets/thumbs/thumb-${post.slug}.svg" alt="${escapeHtml(
    post.title
  )}"><strong>${escapeHtml(post.title)}</strong></a>`;
  const updatedBlock = before + mini + after;
  return indexHtml.slice(0, blockStart) + updatedBlock + indexHtml.slice(blockEnd);
}

async function main() {
  const postsDir = path.join(root, "posts");
  const thumbsDir = path.join(root, "assets", "thumbs");
  const genresDir = path.join(root, "genres");

  await mkdir(postsDir, { recursive: true });
  await mkdir(thumbsDir, { recursive: true });
  await mkdir(genresDir, { recursive: true });

  // Write posts and thumbs
  for (const [i, post] of posts.entries()) {
    await writeFile(
      path.join(postsDir, `${post.slug}.html`),
      buildArticleHtml(post),
      "utf8"
    );
    await writeFile(
      path.join(thumbsDir, `thumb-${post.slug}.svg`),
      thumbSvg(i + 1),
      "utf8"
    );
  }

  // Update index timeline and mini-posts
  const indexPath = path.join(root, "index.html");
  let indexHtml = await readFile(indexPath, "utf8");
  indexHtml = insertAfter(
    indexHtml,
    `<div class="timeline">`,
    posts.map(buildTimelineItem).join("")
  );

  const newestByGenre = {};
  for (const post of posts) {
    if (!newestByGenre[post.genre]) newestByGenre[post.genre] = post;
  }
  for (const key of Object.keys(GENRES)) {
    if (newestByGenre[key]) indexHtml = replaceMiniPost(indexHtml, key, newestByGenre[key]);
  }
  await writeFile(indexPath, indexHtml, "utf8");

  // Update genre pages
  for (const genreKey of Object.keys(GENRES)) {
    const file = GENRES[genreKey].file;
    const genrePath = path.join(genresDir, file);
    let html = await readFile(genrePath, "utf8");
    const subset = posts.filter(p => p.genre === genreKey);
    if (subset.length === 0) continue;
    html = insertAfter(html, `<div class="post-grid">`, subset.map(buildPostCard).join(""));
    await writeFile(genrePath, html, "utf8");
  }


}

