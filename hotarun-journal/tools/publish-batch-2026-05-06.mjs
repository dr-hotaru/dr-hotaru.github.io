function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function formatDateDot(date) {
  const [y, m, d] = date.split("-");
  return `${y}.${m}.${d}`;
}

function xHashtags(post) {
  const base = {
    biology: ["#進化", "#生物学"],
    medicine: ["#医学", "#研究"],
    space: ["#地球科学", "#気候"],
    technology: ["#テクノロジー", "#研究"],
    psychology: ["#行動", "#進化"],
    paleontology: ["#古代DNA", "#地球史"],
  }[post.genre];
  const journalTag =
    post.paper.journal === "Nature Communications"
      ? "#NatureCommunications"
      : "#NatureEcologyEvolution";
  const tags = [...base, journalTag];
  return tags.slice(0, 4).join(" ");
}

function buildXDraft(post) {
  const url = `${baseUrl}/posts/${post.slug}.html`;
  const hook =
    post.genre === "technology"
      ? "「どうしてそうなる？」が材料・AIの設計図になります。"
      : post.genre === "space"
        ? "地球や宇宙の“見えない変数”を一緒に読み解きます。"
        : post.genre === "medicine"
          ? "事実・解釈・限界を分けて、読みやすくまとめました。"
          : post.genre === "paleontology"
            ? "過去のデータから“いま”を考える材料に。"
            : post.genre === "psychology"
              ? "社会や行動の進化を、データで確かめます。"
              : "背景→手法→結果→議論で、要点だけ整理。";
  const text = `${post.title}\n${hook}\n${url}\n${xHashtags(post)}`;
  // Keep roughly 140-220 chars by trimming hook if needed.
  const oneLine = text.replaceAll("\n", " ");
  if (oneLine.length <= 220) return oneLine;
  return `${post.title} ${url} ${xHashtags(post)}`;
}

